<h2>Recipe :Fish Tikka</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>

<h2><b><font color="blue">Ingredients</font></b></h2>

<p class="big">
520gms monkfish fillets cut into 4cm chunks<br />

1 tsp lemon juice<br />

Pinch of salt<br />

4-5 wooden skewers<br />

Melted butter for basting (around 1-2tbsps)<br />

Lemon juice or chaat masala for garnish<br />

Mint chutney to serve<br />

<p><strong>For Marinate</strong></p> 
3 garlic cloves roughly chopped<br />

½" ginger roughly chopped<br />

1 heaped tbsp thick greek yoghurt<br />

½ tsp mild chilli powder (mild paprika would be great or kashmiri chilli powder)<br />

1 tsp cumin powder<br />

¼ tsp coarsely ground white pepper powder<br />

1 heaped tsp chick pea flour<br />

1 tbsp fresh coriander finely chopped<br />

1 tsp vegetable oil<br />

Salt to taste<br />

<h2><b><font color="blue">Directions</font></b></style></h2> 
<p><strong><i>Step 1</i></strong></p> 
    Add the monkfish to a mixing bowl along with the lemon juice and salt. Mix well and set aside.
    
    
 <p><strong><i>Step 2</i></strong></p>   
In a blender add the garlic and ginger along with a splash of water to make a thick smooth paste.

<p><strong><i>Step 3</i></strong></p>
Soak the wooden skewers in warm water until you make the marinade.

<p><strong><i>Step 4</i></strong></p>
Soak the wooden skewers in warm water until you make the marinade.

<p><strong><i>Step 5</i></strong></p>
Preheat the grill  to a medium to high setting. Skewer the fish onto the wooden skewers. Pat the extra marinade over the pieces and place under the grill. Grill for 10-12 mins or until just done. Baste with butter half way through cooking.

Serve the Tandoori fish tikka over roti with a sprinkle of lemon juice or chaat masala & serve with mint chutney.


