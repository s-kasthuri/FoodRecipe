<h2>Recipe : Rendang Ayam</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>


<h2><b><font color="blue">Ingredients</font></b></h2>
<p class="big">
1 1/2 pound boneless and skinless chicken breasts or thighs, cut into cubes<br />
1/3 cup cooking oil<br />
1 cinnamon stick<br />
3 cloves<br />
3 star anise<br />
3 cardamom pods<br />
1 lemongrass, white part only, pounded and cut into strips<br />
1 cup coconut milk<br />
1 cup water<br />
5 kaffir lime leaves, bruised<br />
5 tablespoons toasted grated coconut (kerisik)<br />
1 tablespoon sugar or to taste<br />
Salt to taste<br />


<h3><b><font color="grey">Spice Paste</font></b></h3>
<p class="big">
6 shallots<br />
1-inch piece galangal<br />
3 stalks lemongrass, white part only<br />
4 cloves garlic<br />
1-inch piece ginger, peeled<br />
10 dried chilies (chili arbol), seeded<br />


<h2><b><font color="blue">Directions</font></b></style></h2>      

All all the ingredients of the Spice Paste in a food processor. Blend well.

Heat the oil in a skillet, add the Spice Paste, cinnamon, cloves, star anise, and cardamom pods and stir-fry them until aromatic. Add the chicken and lemongrass, stir to combine well with the spices. Add the coconut milk, water, and simmer on medium heat, stirring frequently until the chicken is almost cooked.

Add the kaffir lime leaves, toasted coconut, stir to blend well with the chicken. Lower the heat to low, cover the lid, and slowly simmer for 30 minutes or until the chicken is tender and the liquid has dried up. Add more sugar and salt to taste to taste. Serve immediately.<br />





