<h2>Recipe :Mutton Briyani</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>

<h2><b><font color="blue">Ingredients</font></b></h2>

<p><strong><i>Marination</i></strong></p>
<p class="big">
1½ kg mutton cleaned and washed well<br />
salt - as per taste<br />
2 tbsp red chilli powder<br />
1 tsp turmeric powder<br />
3 tbsp ginger garlic paste<br />
3-4 small cinnamon sticks(dalchini)<br />
1 tsp caraway seeds(shahi zeera)<br />
3-4 cloves(loung)<br />
1 tsp green cardamom powder(elaichi powder)<br />
1 tsp black pepper corn powder(kali mirchi powder)<br />
2-3 kabab chini(chinese cubeb)<br />
8-10 green chillies<br />
1 large bunch coriander leaves<br />
1 large bunch mint leaves<br />
1 cup of deep fried onions<br />
2 tbsp oil<br />
1 tsp garam masala powder<br />
3 cups yogurt/curd<br />
1 tbsp extracted lemon juice<br />


<p><strong><i>For the Rice</i></strong></p>
<p class="big">
1.25 kg rice
salt to taste
2 medium size cinnamon sticks
2-3 green cardamoms
3-4 cloves
1 tsp caraway seeds
3-4 black pepper corns
water to cook rice
1 tbsp lemon extracted juice

<b>For The Dum and Seasoning:</b>
a large cooking pot
2 tbsp oil to coat the base
¼ cup saffron flavored milk(2 strands of saffron dipped in ¼th cup milk)
2 tbsp chopped coriander leaves for garnising
2 tbsp chopped mint leaves for garnishing
2 tbsp crushed deep fried onions for garnishing
1 tbsp ghee
2 tbsp edible oil
2 tbsp lemon extracted juice

<h2><b><font color="blue">Directions</font></b></style></h2> 
<p><strong><i>Step 1</i></strong></p> 
    When the mutton is marinated for more than 3 hours wash and soak the rice for 40 minutes. Meanwhile take a large cooking pot, add water generously into it. Add salt into the water for taste. Add whole spices such as cinnamon, cardamom, cloves, black pepper, caraway seeds into the water. Bring the water to a boil. When the water boils add soaked rice into it. Cook the rice until it is ¾ th done / 70% done. Strain the rice completely.
    
<p><strong><i>Step 2</i></strong></p> 
Add lemon extracted juice all over the rice. Seal the lid of the cooking pot tightly with a chapathi dough. Place a skillet/tawa below the cooking pot while giving dum.
Dum the biryani for about 15-20 minutes on highfllame and for 40 minutes on sim flame. The biryani gets cooked in an hour. Always take the biryani out from one corner and take it out from the bottom to the top. Serve the biryani with any raita or mirchi ka salan or bagara baingan salan.

