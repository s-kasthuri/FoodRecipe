<h2>Recipe : Satay</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>


<h2><b><font color="blue">Ingredients</font></b></h2>
<p class="big">
4 chicken legs and thighs (preferred) or 4 boneless and skinless chicken breasts<br />
Bamboo skewers, soaked in cold water for 2 hours<br />
1 cucumber, cut into small pieces<br />
1 small onion, quartered<br />
Oil, for basting<br />

<h3><b><font color="grey">Marinade</font></b></h3>
<p class="big">
3 tablespoons oil<br />
2 stalks lemongrass, white parts only<br />
2 cloves garlic, peeled<br />
6 shallots, peeled<br />
2 teaspoons turmeric powder<br />
1 teaspoon coriander powder<br />
1 teaspoon chili powder<br />
1/2 tablespoon salt, or to taste<br />
2 tablespoons sugar<br />

<h2><b><font color="blue">Directions</font></b></style></h2>      

Cut the chicken meat into small cubes. Blend the Marinade ingredients in a food processor. Add in a little water if needed. Marinate the chicken pieces for 6 hours or best overnight. Thread a few pieces of the chicken meat onto the bamboo skewers and grill for 2-3 minutes on each side until nicely charred. Baste and brush with some oil while grilling. Serve hot with the fresh cucumber pieces and onions.<br />

<h2><b><font color="blue">Ingredients for Peanut Sauce</font></b></h2>
<p class="big">
1 1/2 cup dry roasted peanuts (unsalted)<br />
1 cup water<br />
1 tablespoon sweet soy sauce (Kecap Manis)<br />
1 1/2 tablespoon sugar (palm sugar preferred<br />)
1/8 teaspoon salt<br />
1/4 cup oil<br />
1 heaping tablespoon tamarind pulp (soaked in 1/4 cup water for 15 minutes, squeeze the tamarind pulp for juice and discard the pulp)<br />

<h3><b><font color="grey">Spice Paste</font></b></h3>
<p class="big">
6-8 dried red chilies (seeded and soaked in warm water)<br />
3 cloves garlic<br />
3 shallots<br />
2 lemon grass (white parts only)<br />
1 inch ginger (galangal preferred)<br />
1 tablespoon coriander powder (optional)<br />

<h2><b><font color="blue">Directions</font></b></style></h2> 
<p class="big">
Crush the peanuts coursely with mortar and pestle or mini food processor and set aside.
Chop the spice paste ingredients and blend until fine. Heat oil and fry the spice paste until aromatic and smell spicy. Add the peanuts, tamarind juice, water, sugar, sweet soy sauce and stir thoroughly. Simmer in low heat while continue stirring for about 3 minutes until the peanut sauce turns smooth. Serve at room temperature with the satay.<br />




