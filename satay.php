	<p><b>Satay</b> is a grilled meat dish famous throughout Southeast-Asia. The tender meat is sliced and chopped into thin strips or small bite-size pieces, then marinate it in a flavorful mixture of herbs and spices. The meat is then skewered onto wooden sticks (known as satay sticks) and grilled over charcoal or your own backyard BBQ. Satay is traditionally served with satay sauce, which is a fresh peanut sauce (never made with peanut-butter) - a must for any true satay lover. </p></div>
    
