<h2>Recipe :Spicy Tamarind Soup</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>

<h2><b><font color="blue">Ingredients</font></b></h2>


<p class="big">
2 tbsp. toor dal (yellow pigeon peas), rinsed, soaked 30 minutes, and drained<br />
1⁄2 tsp. ground turmeric<br />
6 plum tomatoes, chopped<br />
Kosher salt, to taste<br />
2 tsp. rasam powder<br />
2 tsp. tamarind paste<br />
1 tsp. ground black pepper<br />
1 tsp. ground coriander<br />
1 tsp. ground cumin<br />
1⁄2 tsp. red chile powder, such as cayenne<br />
2 tbsp. canola oil<br />
1⁄4 tsp. black mustard seeds<br />
1⁄4 tsp. cumin seeds<br />
1⁄4 tsp. fenugreek seeds<br />
15 fresh or frozen curry leaves<br />
2 chiles de árbol, stemmed<br />
2 cloves garlic, minced<br />

<h2><b><font color="blue">Directions</font></b></style></h2> 
<p><strong><i>Step 1</i></strong></p> 
    Bring dal, turmeric, tomatoes, salt, and 5 cups water to a boil in a 4-qt. saucepan. Reduce heat to medium-low; cook until dal is mushy, about 45 minutes. Stir in rasam powder, tamarind paste, black pepper, coriander, cumin, and chile powder; cook 5 minutes. Heat oil in an 8" skillet over medium-high. Cook mustard, cumin, and fenugreek seeds, curry leaves, and chiles de árbol until seeds pop, 1–2 minutes. Add garlic; cook until golden, 3–4 minutes, and stir into soup.
