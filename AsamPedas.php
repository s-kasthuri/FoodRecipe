<h2>Recipe : Asam Pedas</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>


<h2><b><font color="blue">Ingredients</font></b></h2>
<p class="big">
8 oz beef flap meat, cut into pieces<br />
2 shallots, sliced thinly (or 1/4 onion, sliced)<br />
2 tablespoons oil<br />
3 tablespoons kicap/kecap manis (sweet soy sauce)<br />
1 1/2 teaspoon soy sauce or to taste<br />


<h3><b><font color="grey">Spice Paste</font></b></h3>
<p class="big">
6 shallots<br />
1-inch piece galangal<br />
3 stalks lemongrass, white part only<br />
4 cloves garlic<br />
1-inch piece ginger, peeled<br />
10 dried chilies (chili arbol), seeded<br />


<h2><b><font color="blue">Directions</font></b></style></h2>      

Rinse the beef with water and cut it across the grain into pieces. Pat dry and set aside.

Heat up a wok with the oil. When the oil is heated, add the shallots and stir fry until aromatic or when the shallots turn light brown. Add the beef into the wok and stir for a few times. Add the sweet soy sauce and soy sauce and stir to blend well with the beef. Cook for a few minutes or until the beef is cooked through. Dish out and serve immediately.<br />

