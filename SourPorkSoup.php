<h2>Recipe : Sour Pork Soup</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>

<h2><b><font color="blue">Ingredients</font></b></h2>

<p class="big">
5 dried wood ear mushrooms
4 dried shiitake mushrooms
8 dried tiger lily buds
4 cups chicken stock
1 teaspoon soy sauce
1/2 teaspoon white sugar
1/2 (16 ounce) package firm tofu, cubed
2 tablespoons thinly sliced green onion

<h2><b><font color="blue">Directions</font></b></style></h2>      
<p><strong><i>Step 1</i></strong></p>
Soak the dried mushrooms and tiger lily buds in warm water for 20 minutes. After trimming off any tough stems, slice the mushrooms. With the fingers, shred the tiger lily stems

<p><strong><i>Step 2</i></strong></p>
Place the mushrooms, tiger lily buds, stock, bamboo shoots, and shredded pork into a saucepan. Bring to a boil, and simmer for 10 minutes.

<p><strong><i>Step 3</i></strong></p>
Stir in soy sauce, sugar, salt, white pepper, and vinegar. Combine cornstarch with 3 tablespoons water. Add a little of the hot soup to the cornstarch, and then return all to the pan. Heat to boiling, stirring. Add the bean curd, and cook 1 to 2 minutes.

<p><strong><i>Step 4</i></strong></p>
 In a small bowl, add salt, sugar and rice vinegar. Mix thoroughly with warm water.
 
<p><strong><i>Step 5</i></strong></p>
Just before serving, turn off the heat. Stir the egg in gradually. Mix in sesame oil. Sprinkle each serving with scallions.
 

