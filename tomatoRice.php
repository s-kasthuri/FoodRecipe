<h2>Recipe : Tomato Rice</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>


<h2><b><font color="blue">Ingredients</font></b></h2>


<p class="big">
1 cup basmati rice, rinsed until water runs clear<br />
1⁄4 cup canola oil<br />
1 tsp. black mustard seeds<br />
1⁄4 tsp. ground turmeric<br />
6 fresh or frozen curry leaves<br />
3 chiles de árbol, stemmed<br />
2 cloves garlic, thinly sliced<br />
1 small yellow onion, minced<br />
3 plum tomatoes, cored and roughly chopped<br />
1⁄4 tsp. asafoetida<br />
Kosher salt, to taste<br />


<h2><b><font color="blue">Directions</font></b></style></h2> 
<p><strong><i>Step 1</i></strong></p> 
    
Bring 6 cups water to a boil in a 4–qt. saucepan. Add rice; cook, stirring occasionally, until rice is tender, 10–12 minutes. Drain rice and transfer to a bowl. Add oil to pan; heat over medium-high. Cook mustard seeds, turmeric, curry leaves, and chiles until seeds pop, 1–2 minutes. Add garlic, onion, and tomatoes; cook until tomatoes begin to break down, 6–8 minutes. Add asafoetida and salt; stir into rice.
