<h2>Recipe : Nasi Lemak</h2>
<ul>
<li><font color="#FFFFFF">Time: 25 Minutes</font></li><br />
<li><font color="#FFFFFF">Serves: 4 Person's</font></li>
</ul>

<h2><b><font color="blue">Ingredients</font></b></h2>

<h3><b><font color="grey">Coconut Milk Steamed Rice</font></b></h3>

<p class="big">
2 cups of rice<br>
3 screwpine leaves (tie them into a knot as shown above)<br>
Salt to taste<br>
1 small can of coconut milk<br>
Some water<br>

<h3><b><font color="grey">Sambal Ikan Bilis (Dried anchovies sambal)</font></b></h3>

<p class="big">
1/2 red onion<br>
1 cup ikan bilis (dried anchovies)<br>
1 clove garlic<br>
4 shallots<br>
10 dried chillies<br>
1 teaspoon of belacan (prawn paste)<br>
1/4 teaspoon of salt<br>
1 tablespoon of sugar<br>

<h3><b><font color="grey">Other Ingredients</font></b></h3>

<p class="big">
2 hard boiled eggs (cut into half)<br>
3 small fish (sardines or smelt fish)<br>
1 small cucumber (cut into slices and then quartered)<br />




 
<h2><b><font color="blue">Directions</font></b></style></h2>      
<p><strong><i>Step 1</i></strong></p>
In a medium saucepan over medium heat, stir together coconut milk, water, ground ginger, ginger root, salt, bay leaf, and rice. Cover, and bring to a boil. Reduce heat, and simmer for 20 to 30 minutes, or until done.<br>

<p><strong><i>Step 2</i></strong></p>
Place eggs in a saucepan, and cover with cold water. Bring water to a boil, and immediately remove from heat. Cover, and let eggs stand in hot water for 10 to 12 minutes. Remove eggs from hot water, cool, peel and slice in half. Slice cucumber.<br>

<p><strong><i>Step 3</i></strong></p>
Meanwhile, in a large skillet or wok, heat 1 cup vegetable oil over medium-high heat. Stir in peanuts and cook briefly, until lightly browned. Remove peanuts with a slotted spoon and place on paper towels to soak up excess grease. Return skillet to stove. Stir in the contents of one package anchovies; cook briefly, turning, until crisp. Remove with a slotted spoon and place on paper towels. Discard oil. Wipe out skillet.<br>

<p><strong><i>Step 3</i></strong></p>
Heat 2 tablespoons oil in the skillet. Stir in the onion, garlic, and shallots; cook until fragrant, about 1 or 2 minutes. Mix in the chile paste, and cook for 10 minutes, stirring occasionally. If the chile paste is too dry, add a small amount of water. Stir in remaining anchovies; cook for 5 minutes. Stir in salt, sugar, and tamarind juice; simmer until sauce is thick, about 5 minutes.<br>

<p><strong><i>Step 3</i></strong></p>
Serve the anchovies sauce(sambal ikan bilis) over the warm rice, and top with peanuts, fried anchovies, cucumbers, and eggs.<br>

