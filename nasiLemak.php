<div class="center">
  <p><b>Nasi lemak</b> is Malay for “rice in cream”, a reference to the rice being cooked in coconut milk, or “richly flavoured rice”.The rice is lightly salted and made fragrant with a knot of pandan leaves added while the rice is still cooking. It is the ordinary man’s breakfast, traditionally served with fried fish known in Malay as ikan bilis (anchovies),and a dollop of sambal (a type of chilli paste). The anchovies are fried so crisp that it can be eaten whole. Nowadays, the anchovies are fried with salted peanuts, the dish topped with thin slices of cucumber and a fried or boiled egg.The rice and all its condiments and side dishes are kept warm in a banana leaf folded into a conical pocket.
